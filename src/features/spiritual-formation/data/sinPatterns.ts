function buildPractice(patternId: SinPatternId, name: string, category: PracticeCategory, frequency: "daily" | "weekly" | "as_needed"): Practice {
  const id = `${patternId}_${name.toLowerCase().replace(/[^a-z0-9]+/g, "_").replace(/^_|_$/g, "")}_${frequency}`;
  return createPractice({
    id,
    name,
    category,
    frequency,
    estimatedMinutes: frequency === "weekly" ? 25 : frequency === "as_needed" ? 5 : 10,
    description: `${name} for the ${patternId.replaceAll("_", " ")} pattern.`,
    instructions: ["Pause before God.", "Name what is happening honestly.", "Ask the Holy Spirit for help.", "Choose one concrete act of obedience."],
  });
}

function makePattern(seed: PatternSeed): SinPattern {
  const category = seed.practiceCategory ?? "prayer";
  return {
    ...seed,
    scriptures: seed.scriptures.map((reference) => ({ reference, text: scriptureText[reference] ?? reference })),
    dailyPractices: seed.dailyPracticeNames.map((name) => buildPractice(seed.id, name, category, "daily")),
    emergencyPractices: seed.emergencyPracticeNames.map((name) => buildPractice(seed.id, name, category, "as_needed")),
    weeklyPractices: seed.weeklyPracticeNames.map((name) => buildPractice(seed.id, name, category, "weekly")),
  };
}

